class Track {
    id;
    name;
    courseList = [];
    constructor(track) {
        this.name = track.name
        this.id = track.id
        track.courses.map(
            (courses) => {
                this.courseList.push(new Course(courses))
            }
        )
    }
};


class Course {
    id;
    name;
    topicsList = [];
    constructor(course) {
        this.name = course.name
        this.id = course.id
        course.topics.map(
            (topic) => {
                this.topicsList.push(new Topic(topic))
            }
        )
    }
};


class Topic {
    id;
    name;
    setsList = [];
    constructor(topic) {
        this.name = topic.name
        this.id = topic.id
        topic.sets.map(
            (set) => {
                if (set.type == "McqSets")
                    this.setsList.push(new McqSet(set))
                if (set.type == "PracticeSets")
                    this.setsList.push(new PracticeSet(set))
                if (set.type == "LearningSets")
                    this.setsList.push(new LearningSet(set))
                if (set.type == "AssgnmentSets")
                    this.setsList.push(new AssignmentSet(set))
                if (set.type == "CodingSets")
                    this.setsList.push(new CodingSet(set))

            }
        )
    }
};

class Set {
    id;
    name;
    type;
    content;
    constructor(set) {
        this.id = set.id;
        this.name = set.name
        this.type = set.type
        this.content = set
    }
}


